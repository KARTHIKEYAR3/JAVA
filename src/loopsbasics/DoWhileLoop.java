package loopsbasics;

public class DoWhileLoop {
	public static void main(String[] args) {
		
		int i=1;
		 do{
			System.out.println(i);
			 i++;

		}while(i<=10);
	}
}
// In Do-While Loop do prints the statement before it checks 
// while the condition.
// do prints the statement once whether the condition true or false.