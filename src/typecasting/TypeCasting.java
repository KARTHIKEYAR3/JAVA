package typecasting;

public class TypeCasting {
	public static void main(String[] args) {
		
		String str="200";
		
		System.out.println(str+100);
		int a= Integer. parseInt(str);    //changing the string into integer
	    System.out.println(a+100);
	}   
	

}




//type casting is used for changing string number into integer number.